package com.bklkj.Commons;


public class Mine {
	private int code;
	// 提示信息
	private String message;

	private String summoney;
	
	private String userpicture;
	
	private String withdrawalsmoney;
	
	private String usernickname;
	
	public String getSummoney() {
		return summoney;
	}

	public void setSummoney(String summoney) {
		this.summoney = summoney;
	}

	public String getWithdrawalsmoney() {
		return withdrawalsmoney;
	}

	public void setWithdrawalsmoney(String withdrawalsmoney) {
		this.withdrawalsmoney = withdrawalsmoney;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	

	public String getUserpicture() {
		return userpicture;
	}

	public void setUserpicture(String userpicture) {
		this.userpicture = userpicture;
	}

	

	public String getUsernickname() {
		return usernickname;
	}

	public void setUsernickname(String usernickname) {
		this.usernickname = usernickname;
	}


	
	
}
