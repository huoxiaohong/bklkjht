package com.bklkj.Commons;


public class Joinactivity {
	
	private int code;
	// 提示信息
	private String message;
	
	private String money;
	
	private String name;
	
	private String web;
	
	private String logo;
	
	private String businessintroduce;
	
	public String getBusinessintroduce() {
		return businessintroduce;
	}
	public void setBusinessintroduce(String businessintroduce) {
		this.businessintroduce = businessintroduce;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWeb() {
		return web;
	}
	public void setWeb(String web) {
		this.web = web;
	}
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
}
